public class chap04_02 {
    //switchDemo p.111
    public static void main(String[] args) {
        int data = 1;

        switch(data){
            case 1:
            System.out.println("1");
            break;

            case 2:
            System.out.println("2");
            break;

            default:
            System.out.println("default");
        }
    }
}
//break 문 빼먹지 말자.